def set_cell_bg(cell, hex_color):
    tcPr = cell._tc.get_or_add_tcPr()
    shd = OxmlElement('w:shd')
    shd.set(qn('w:val'), 'clear')
    shd.set(qn('w:color'), 'auto')
    shd.set(qn('w:fill'), hex_color)
    tcPr.append(shd)


def para(text='', size=11, bold=False, italic=False, color=None, align=None,
         space_after=6, space_before=0):
    p = doc.add_paragraph()
    if align:
        p.alignment = align
    p.paragraph_format.space_after = Pt(space_after)
    p.paragraph_format.space_before = Pt(space_before)
    if text:
        r = p.add_run(text)
        r.font.size = Pt(size)
        r.bold = bold
        r.italic = italic
        if color:
            r.font.color.rgb = color
    return p


def rich(parts, size=11, space_after=6, space_before=0, color=None):
    p = doc.add_paragraph()
    p.paragraph_format.space_after = Pt(space_after)
    p.paragraph_format.space_before = Pt(space_before)
    for text, opt in parts:
        r = p.add_run(text)
        r.font.size = Pt(opt.get('size', size))
        r.bold = opt.get('bold', False)
        r.italic = opt.get('italic', False)
        c = opt.get('color', color)
        if c:
            r.font.color.rgb = c
    return p


def bullet(text, size=11):
    p = doc.add_paragraph(style='List Bullet')
    p.add_run(text).font.size = Pt(size)
    p.paragraph_format.space_after = Pt(3)
    return p


def h(text, level=1):
    p = doc.add_heading(text, level=level)
    for r in p.runs:
        r.font.color.rgb = ACCENT
    return p


def make_table(header, rows, widths_pt=None, header_fs=9, body_fs=9):
    t = doc.add_table(rows=1, cols=len(header))
    t.alignment = WD_TABLE_ALIGNMENT.CENTER
    t.style = 'Light Grid Accent 1'
    hc = t.rows[0].cells
    for i, txt in enumerate(header):
        hc[i].paragraphs[0].clear()
        r = hc[i].paragraphs[0].add_run(txt)
        r.bold = True
        r.font.size = Pt(header_fs)
        r.font.color.rgb = RGBColor(0xFF, 0xFF, 0xFF)
        set_cell_bg(hc[i], '1F4E79')
    for row in rows:
        cells = t.add_row().cells
        for i, txt in enumerate(row):
            cells[i].paragraphs[0].clear()
            r = cells[i].paragraphs[0].add_run(txt)
            r.font.size = Pt(body_fs)
    return t


